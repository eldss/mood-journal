"use client"
import { DefaultError } from "@/components/DefaultError"
import { useEffect } from "react"

export default function Error({
  error,
  reset,
}: {
  error: Error & { digest?: string }
  reset: () => void
}) {
  useEffect(() => {
    console.error(error)
  }, [error])

  return <DefaultError reset={reset} />
}
